// 描述
// 输入一个整数 n ，输出该数32位二进制表示中1的个数。其中负数用补码表示。

// 数据范围：- 2^{31} <= n <= 2^{31}-1−2 31 <=n<=2 31 −1
// 即范围为:-2147483648<= n <= 2147483647−2147483648<=n<=2147483647

func NumberOf1(n int) int {
	var res int
	for i := 0; i < 32; i++ {
		if (n & (1 << i)) != 0 {
			res++
		}
	}
	return res
}
